"""soma.py contém as funções utilizadas para realizar a soma
entre dois números.
"""
def soma_inteiros(x: int, y: int)-> int:
    """Realiza a soma entre dois inteiros.
    
    Parameters
    ----------
    x : int
        número inteiro.
    y : int
        número inteiro
    
    Returns
    -------
    int
        o resultado da soma de x + y
    """
    return x + y

def soma_reais(x: float, y: float)-> float:
    """Realiza a soma entre dois reais.
    
    Parameters
    ----------
    x : float
        número real.
    y : float
        número real.
    
    Returns
    -------
    float
        o resultado da  soma x + y.
    """
    return x + y
